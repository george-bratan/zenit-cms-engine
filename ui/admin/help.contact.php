
<div class="help">
	<h1>Contact Messages</h1>

	<p>
		The <strong>Contact Message</strong> section allows you to setup a contact form on your public site. Available fields for the form are: Name, Email,
		Subject and Message body. With every contact message you receive, a support thread is created in your Application, and a unique ID is given to the
		thread. Once you reply on a thread, an email is sent to the contact's email address with your reply and a public link where he/she may access the
		entire message thread.
	</p>

	<p>
		You can use the <strong>Subjects</strong> section to define message topics. These will be relayed to the contact form page on the public site and the
		user will have to select a message topic/subject when submitting the contact form. By default, the Subject dropdown is included in the contact form,
		but you can choose not to include it if you don't need one. See <strong>Content Publishing - Content Blocks</strong> for more information about
		including a contact form in your public site.
	</p>

	<p>
		You can use default replies to speed up resolving common or recurring issues. Just select a default reply from the comment dropdown
		and its contents will be pasted in your reply textbox.
	</p>

	<p>
		Default <strong>Replies</strong>, contact <strong>Topics</strong> and <strong>Labels</strong> can be edited and managed from their respective section.
	</p>

	<p>
		The <strong>Settings</strong> section contains your public thread URL. This should point to a page containing an <strong>@uid</strong> url code,
		symbolizing the unique message id, and the page should be setup from the <strong>CMS module</strong> to render a contact message thread. You can read
		more on working with the CMS from the CMS section of the manual.
	</p>

</div>
