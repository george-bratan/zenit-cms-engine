
<div class="help">
	<h1>Dashboard</h1>

	<p>
		The <strong>Dashboard</strong> offers an overview of your current system status.
		By default, it contains links to your most used features and a graphic report area.
	</p>

	<p>
		The graphic area renders data pulled from any module able to provide a <strong>Timeline Feed</strong>. Current known feeds are:
	</p>

	<ul>
		<li>CMS: Page Views as recorded by the Application</li>
		<li>CMS: Unique Visitors as recorded by the Application</li>
		<li>CMS: Page Views as recorded by Google Analytics (see CMS - Settings for setup)</li>
		<li>CMS: Unique Visitors as recorded by Google Analytics (see CMS - Settings for setup)</li>
		<li>eShop: Number of orders received on a daily basis</li>
		<li>Newsletter: Number of new daily subscribers
		<li>Support: Number of new tickets submitted (viewable with your current access level)</li>
	</ul>
</div>
