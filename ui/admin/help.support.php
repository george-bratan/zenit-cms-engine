
<div class="help">
	<h1>Support System</h1>


	<p>
		The main section found in your <strong>Support</strong> module is the <strong>Tickets</strong> tab. This is where you will manage all support issues
		related to your company.
	</p>

	<p>
		A <strong>Ticket</strong> can be setup to contain:
		<ul>
			<li><strong>Multiple Companies</strong>. This grants access to all selected companies to the specified ticket.</li>
			<li><strong>Multiple Comment Threads</strong>. For every ticket created you will get two default threads: one Public where ALL companies
				involved in a ticket can read and comment, and one Private thread where only your Company may read and comment.
				<ul>
					<li>Note: New threads can be created at any given time. Click "<strong>New Thread</strong>" in the upper-right corner of your comment list and select
						the Companies you wish to allow access to the new thread. Example: If you open a new thread and only select Company A and Company B for that thread,
						Company C will NOT be able to see or comment.</li>
					<li>All user accounts of a Company will have access to the Company's tickets and threads.</li>
				</ul></li>
			<li><strong>Multiple Quotes</strong> can be sent out to the companies involved in a ticket, based on the conversation unfolding in the comment threads.
				Quotes will only appear to the sender and recipient company. They initially enter a Pending state until the recipient Approves or Rejects them.
				<ul>
					<li>Note: Though theoretically only approved quotes should be available for invoicing, due to practical realities the system does not enforce this rule:
						All quotes are available for assignment to invoices.</li>
				</ul>
			<li><strong>Multiple Time Entries</strong> can be defined at any given time, accounting for the time spent on a ticket's tasks.
				These can be later studied for statistical and management data.</li>
			<li><strong>Multiple Files</strong> may be attached.
				<ul>
					<li>Files may also be uploaded through the comment form, if the file is relevant to the comment itself, rather than the ticket.
						The file will be then available for download next to your comment.</li>
				</ul>
		</ul>
	</p>

	<p>
		The <strong>Quotes</strong> and <strong>Time Entries</strong> tabs lists all your recent records and allow you to overlook the current support status,
		identify orphan or unpaid quotes and provide statistic data relating to your time entries.
	</p>

	<p>
		The <strong>Invoicing</strong> tab lists all invoices you sent or received. Use the <strong>Series</strong> and <strong>Index</strong> boxes to setup
		identifiable information for your documents. For example, you can use different series for different companies you're invoicing, while keeping track of
		their order through their Index number.
	</p>

	<p>
		<strong>Invoices</strong>
		are defined by selecting a subset of <strong>Quotes</strong> that were previously sent by your company to the invoiced company. By default, when you
		create a new invoice, all quotes relating to the invoiced company that have not been previously assigned to any other invoice will be included in the
		new document. You may choose to remove or attach individual new quotes.
	</p>

	<p>
		Note that <strong>Quotes</strong> are the building blocks for an invoice, while <strong>Time Entries</strong> are only used for statistical purposes.
		An invoice file is also required for the record to be complete.
	</p>

	<p>
		Using the <strong>Users</strong> tab you can manage all users registered with a support account under your company name. Along with the usual features
		to create, delete and update records, you may also setup individual access rights to your company's user accounts.
	</p>

	<p>
		The <strong>Companies</strong> tab lists all companies that you are connected to and are registered with a support account. The company creation button
		enables a new blank company account and sends an invitation to the newly setup company email, with details to login and create new users. The newly
		created company will be connected to you and will be available to be included in your tickets.
	</p>

	<p>
		The <strong>Settings</strong> tab allows you to enable email notifications. When enabled, you will receive an email for every new comment on tickets
		where your company is included.
	</p>

</div>
