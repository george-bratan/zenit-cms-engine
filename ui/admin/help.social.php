
<div class="help">
	<h1>Social Media</h1>

	<p>
		If your business requires constant social interactions Zenit Systems allows you to manage your <strong>Facebook</strong> account,
		<strong>Twitter</strong> account and <strong>YouTube</strong> Channel straight from your Administration Console, through the use of remote API connectivity.
	</p>

	<p>
		By using the <strong>Social Media</strong> module, you will be able to grant access to multiple employees while keeping your passwords safe (you never
		have to share your social passwords with anyone). You will also be able to keep your personal and business accounts separate: while you're logged in
		with your personal account on Facebook, the Web Application will be logged in with your business account.
	</p>

	<p>
		You can grant access to Zenit CMS on Facebook, Twitter or YouTube through the <strong>Settings</strong> section of your Social Media module. Once you
		grant access, the Application will pull your latest Facebook wall, Twitter comments, and YouTube channel, together with all comments. This data is not
		stored or cached in any way by Zenit and are pulled through the APIs provided by their respective service.
	</p>

	<p>
		You will be able to submit posts to <strong>Facebook</strong> and reply to comments on your wall.
		Similar features are enabled for your <strong>Twitter</strong> account: tweet, retweet and reply.
		On your <strong>YouTube</strong> section, you will be able to upload videos, edit current video details and view/reply to comments.
	</p>

	<p>
		Please note that your Facebook, Twitter or YouTube passwords are not required for this integration, they are not requested, obtained or stored at any
		time by Zenit Systems.
	</p>

</div>
