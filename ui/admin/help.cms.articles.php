
<div class="help">
	<h1>Article Publishing</h1>

	<p>
		<strong>Articles</strong>
		are your main dynamic content tool. These are versatile entities, holding common attributes and can be used as news pieces, events or blog posts.
	</p>

	<p>
		An article may hold:
		<ul>
			<li>Article Body. If you want to provide an article snippet similar to a short description,
				include a horizontal rule "<strong><em>&lt;hr&gt;</em></strong>" in your article body.
				All content up to the <strong>&lt;hr&gt;</strong> tag will be considered a short description and a "Read More ..." link will be provided.</li>
			<li>General attributes: title, keyword, category, publication date.
				<ul>
					<li>Note: the publication date is a static date field and does not prevent the article from being published until the set date is reached.
						You will have to manually enable/disable an article.</li>
				</ul></li>
			<li>Event specific attributes: start and end dates</li>
			<li>Contact details: name, email, phone and website url</li>
			<li>Location specific details: venue, location address, and a map marker</li>
			<li>Multiple Links. If your article references additional information on the web, you may enter their title and web location in the links section.
				All links will be included in your public article page.</li>
			<li>Multiple Images. Click the Upload button or simply drag-and-drop a selection of images to the Images box and they will be promptly uploaded
				to your article. You can edit an image title and description.</li>
			<li>Enable Comments. If enabled, a comment form will be rendered on your public site under your article body and you will be able moderate
				all comments.</li>
		</ul>
	</p>

	<p>
		For information on setting up the layout of your articles on the public site, please refer to <strong>Content Publishing - Content Blocks</strong>. The
		Articles section exports a <strong>Data-Feed</strong> to the CMS module, and also provides a default layout which you can use. If you want to customize
		this layout, use the <strong>Content Blocks</strong> section to load the Article List or Article Details data-feed and define your custom HTML layout.
	</p>

	<p>
		All comments you receive on published articles are collected and displayed in the <strong>Comments</strong> tab, awaiting your moderation. Click the
		comment status to toggle between <em>Published</em> or <em>Rejected</em>.
	</p>

	<p>
		You can organize your articles into <strong>Categories</strong> and <strong>Labels</strong>. While you can only assign one Category to an article, you
		can assign multiple Labels. These have a functional value for your administration console, and a public filtering value. Categories and Labels can be
		relayed to the public site where visitors can choose to filter your content through them.
	</p>

</div>
