	<h1>Contacts Manager (CRM)</h1>


	<p>
		The <strong>CRM</strong> acts as your central Contact storage and management module.
	</p>

	<p>
		Every <strong>Contact</strong> is represented by a <strong>Virtual Card</strong> (or VCard), holding the contacts primary information: Name, Address,
		Phone and Email. The Phone and Email are linked to generate an appropriate response from the system: clicking the phone number will prompt you to call
		said number through Skype, while clicking the Email will prompt you to launch an email application and send a message.
	</p>

	<p>
		You can also add a note straight from the VCard of a Contact. <strong>Notes</strong> are usually a result of contacting the respective person, or a
		simple reminder that his/her status is changed in a way. All notes are arranged in a thread and can be observed in the Contact Details screen.
	</p>

	<p>
		<strong>Contact Reasons</strong> and <strong>Resolutions</strong> may be defined and edited from the <strong>Labels</strong> section.
	</p>

	<p>
		Multiple addresses may be defined for a single Contact, but only two of them can be selected as current Billing or Postal Address for a person. People
		may use additional addresses for family gifts or presents, and you are able keep these on file for future use.
	</p>

	<p>
		Contacts may be organized with the use of <strong>Companies</strong> and <strong>Labels</strong>. While one contact may belong to to single Company,
		multiple labels can be attached to a Contact.
	</p>

	<p>
		The <strong>Calendar</strong> section lists all notes and contact attempts, together with their reason and resolution for the currently selected date.
		This allows the administrator to keep an overview of a day's events.
	</p>

</div>
