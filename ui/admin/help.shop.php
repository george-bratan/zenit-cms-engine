
<div class="help">
	<h1>eCommerce Shop</h1>



	<p>
		<strong>Orders</strong>
		can be entered through the public website (using the Order Form object, see CMS section), or through the administration console.
	</p>

	<p>
		An order is attached to a <strong>CRM Contact</strong> and can contain multiple products, discounts and taxes. Multiple transactions can be setup for
		an order, splitting the owed amount into multiple smaller payments. Similarly, multiple deliveries can be setup for subscription based orders.
	</p>

	<p>
		The <strong>Deliveries</strong> section lists all deliveries scheduled for the selected date. This allows an administrator to keep an eye on a day's
		events and remaining deliveries.
	</p>

	<p>
		The <strong>Products</strong> section is designed for minimal (and general) information. Any additional product attributes will need to be defined
		based on a custom business requirement. Besides product title, description, category and keywords, multiple images may be attached either through file
		selection or simple drag-and-drop from your computer to the file upload area. You can edit an image title, description and re-order by drag-and-drop.
	</p>

	<p>
		<strong>Categories</strong>
		are logical containers used in product management. These are simple labels and have no direct effect other than filtering through category selection.
	</p>

	<p>
		<strong>Discounts</strong>
		and <strong>Taxes</strong> can be defined as <em>Flat Values</em> or <em>Percentage</em> from the base total. Remember that they both apply to the
		base order value. Ex: if an order contains products totaling 100 currency units, after applying a discount this value drops to 90 currency units; the
		tax applied next will be computed considering the order at 100 currency units.
	</p>

	<p>
		The <strong>Settings</strong> section allows the setup of your <strong>PayPal</strong> details and currency used throughout the eShop module. Your
		PayPal details can be obtain by logging into your secure account with PayPal, and accessing: My Account - Profile - My Selling Tools - API Access -
		Option 2, View API Signature.
	</p>

	<p>
		<em>Note: Your PayPal account must be registered with <strong>PayPal Website Payments Pro</strong>.</em>
	</p>

	<p>
		Note that transactions submitted to PayPal will also be carrying the same currency code so this must be a valid 3 letter international currency code.
		Ex: USD, EUR, AUD, GBP, etc.
	</p>

</div>
